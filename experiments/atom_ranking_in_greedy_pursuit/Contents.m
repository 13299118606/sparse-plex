
Experiments related to Atom Ranking Paper
============================================


The example for motivation is demonstrated in 
the file ``ex_omp_1.m``. 

For the discovery of atoms in regular OMP,
use matching_mode =1.

For the discovery of atoms in the regular
OMP but correlations sorted on the previous
iteration, use matching_mode=2.

Increase the number of trials to 10 and
see trial 7 to see a bad case where
the best match appears quite late in the
sorted list.

