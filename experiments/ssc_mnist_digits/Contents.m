Readme
===================


Load the dataset first: 

$load_db()

After this the MNIST dataset will be available in md variable.


Running the bench on SSC OMP: 

$bench_ssc_omp(md)


Running the bench on SSC-MC-OMP

$bench_ssc_mc_omp(md)


Running the bench on SSC-Batch-OMP

$bench_ssc_batch_omp(md)
